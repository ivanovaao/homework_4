console.log (null == '');//false
//1. выполняется нестрогое сравнение пустого значения со строкой (false==true), что дает результат false

console.log (3 ** (9 / 3));//27
//1. выполняется операция деления чисел 9 на 3, что дает результат число 3
//2. число 3 возводится в степень 3, что дает результат 27

console.log (!!'false' == !!'true');//true
//1. преобразование строк в булевый тип: строка - это true; ! - вернет false; !! - вернет true
//2. выполняется нестрогое сравнение двух логических значений true, что дает результат true

console.log (0 || '0' && 1);//1
//1. сначала выполняется оператор "и", что дает результат 1 так так как оба операнда true
//2. выполняется операция "или" 0 (false) или 1 (true) дает результат 1

console.log ((+null == false) < 1);//false
//1. унарный оператор + вместе с null дает 0
//2. 0 равняется false, что дает результат true
//3. сравнение true < 1 дает результат false, так как true равно 1.

console.log (false && true || true);//true
//1. сначала выполняется оператор "и", что дает результат false
//2. потом выполняется опертор "или", что дает результат true

console.log (false && (false || true));//false
//1. сначала выполняется оператор "или" в скобочках, что дает результат true
//2. далее выполняется оператор "и", что дает результат false

