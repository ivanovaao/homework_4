console.log ('number' + 3 + 3); // number33
//1. преобразование каждого числа в строку
//2. объединение три строки вместе, что дает результат number33

console.log(null + 3);//3
//1. null преобразуется в 0
//2. выполняется операция сложения: 0 + 3 = 3

console.log (5 && "qwerty"); //qwerty
//1. система преобразует 5 в true и строку в true
//2. все значения истиноподобны, тогда возвращается значение последнего операнда - это qwerty

console.log (+ '40' + +'2' + "hillel");//42hillel
//1. унарный оператор сложения применяется к строкам 40 и 2 - в результате чего получаем числа 40 и 2 типа Number
//2. далее выполняется сложение чисел 40+2
//3. преобразование числа 42 в строку и конкатенация со со строкой hillel

console.log ('10' - 5 === 6);//false
//1. преобразварние 10 в число
//2. выполнение операции вычитания 10-5
//3. операнд 5 строго не равен опреанду 6, поэтому возвращается логическое значение false

console.log (true + false);//1
//1. системой преобразовывается как: 1 + 0 = 1

console.log ('4px' - 3);//NaN
//1. из строки отнять число 3 - это недопустимая операция (в итоге: получаем NaN и тип данных число).

console.log ('4' - 3);//1
//1. строка преобразовывается в число 4
//2. операция вычитания двух чисел 4 и 3 дает результат 1.

console.log ('6' + 3 ** 0);//61
/*1. число 3 возводится в нулевую степень, что равняется единице
2. единица преобразуется в строку
3. конкатенация двух строк 6 и 1*/

console.log (12 / '6');//2
//1. строка преобразуется в число 6
//2. деление 12 на 6 дает результат 2

console.log ('10' + (5 === 6));//10false
//1. операнд 5 строго не равен операнду 6 , что дает результат false
